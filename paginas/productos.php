					las burbujas colapsan repentinamente debido al cambio de presión y debido a esto se
					genera una liberación de energía que incrementa la temperatura y presión la que favorece
					la reacción de formación del biodiesel.</p><br>
					<p>Se ha demostrado que la cavitación es efectiva para acelerar la síntesis de biodiésel en
					condiciones ambientales y que la aplicación de la cavitación a la reacción de esterificación
					de aceites permite obtener tiempos menores de reacción que en un reactor convencional.</p><br>
					<p>Además, la aplicación de cavitación hidrodinámica en la producción de biodiésel
