					el aceite y el aditivo se produzca instantáneamente tanto en procesos de esterificación
					como de transesterificación. La idea principal consiste en generar microemulsiones que aumenten el área de contacto y, por lo tanto, las cinéticas de reacción. Como la eficacia de
